<h4>Pantalla principal</h4>
<p><img src="bocetos/bocetoescogeruni.png" title="Pantalla principal Escoli" alt="Pantalla principal Escoli"></p>
<p>La pantalla principal constará de un menú con unas ventanas para elegir la universidad a la que se quiere acceder.
    También hay buscador por el cual el usuario podrá encontrar al profesor deseado.
</p>

<h4>Pantalla facultad</h4>
<p><img src="bocetos/EscogerFacultad.png" title="Pantalla escoger facultad" alt="Pantalla escoger facultad"></p>
<p>Esta página contiene una lista de las facultades disponibles de la universidad elegida. Cada facultad está acompañada
    del profesor mejor valorado de la misma.
</p>

<h4>Muro facultad</h4>
<p><img src="bocetos/bocetomurofacultad.png" title="Muro facultad" alt="Muro facultad"></p>
<p>En esta página se publicarán todos los comentarios relacionados con los profes de esa facultad. Así mismo, será visible
    un apartado con los profesores mejor valorados.
</p>

<h4>Perfil alumno</h4>
<p><img src="bocetos/bocetoperfilAlumno.jpeg" title="Perfil alumno" alt="Perfil alumno"></p>
<p>Esta página mostrará el perfil de un usuario alumno. En ella saldrá su nombre, una biografía, sus amigos y
    un muro con sus comentarios (que puedes filtrar o buscar por profesor). También saldrán los comentarios más valorados.
    Si el que ve la página es ese mismo usuario tendrá disponible el acceso a una página para editar su perfil y su cuenta.
</p>

